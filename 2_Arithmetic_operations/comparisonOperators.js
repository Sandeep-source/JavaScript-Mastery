// Comparison Operators ==> true / false

const a = 5;
const b = 10;

// Greater than
console.log(a > b);

// Greater than equal to
console.log(a >= b);

// Less than
console.log(a < b);

// Less than equal to
console.log(a <= b);

// Is equal
console.log(a == b);

// Not equal
console.log(a != b);

// Strict Equality
console.log(a === b);

// Strict Inequality
console.log(a !== b);
